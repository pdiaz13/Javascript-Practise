//Comparaciones

//Igualdad
if (5 == 5){
    console.log("5 es igual a 5");
}

// == solo compara valor
//=== compara valor y tipo

if (5 === 5){
    console.log("5 es muy igual a 5");
}

let a = 4;
let b = 10;

if (a != b){
    console.log("a es diferente a b -D");
}
// != diferente valor
// !== deferente valor y tipo

if (a !== b){
    console.log("a es diferente a b -F");
}

