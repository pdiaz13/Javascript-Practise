// if else + if else

let nota = 5;

if (nota === 5){
    console.log("Has obtenido un sobresaliente");
} else if (nota === 4){
    console.log("Buen trabajo");
}  else if (nota === 3){
    console.log("Has obtenido un suficiente");
}  else if (nota === 2){
    console.log("No has aprobado");
}  else if (nota === 1){
    console.log("Trabaja un poco mas");
} else {
    console.log("Error, introduce una nota entre el 1-5");
}